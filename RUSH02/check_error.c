#include <unistd.h>

long long int	check_error(char *str)
{	
	while (*str != '\0')
	{
		if (*str < '0' || *str > '9')
		{
			write(1, "Error\n", 6);
			return (1);
		}
		str++;
	}
	return (0);
}
