int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	check;

	check = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			check++;
		}
		i++;
	}
	if (check == i++)
		return (1);
	return (0);
}
