int	ft_str_is_printable(char *str)
{
	int	i;
	int	check;

	check = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 32 && str[i] <= 126) | (str[i] == ' '))
		{
			check++;
		}
		i++;
	}
	if (check == i)
		return (1);
	return (0);
}
