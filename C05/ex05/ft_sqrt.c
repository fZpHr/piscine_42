int	ft_sqrt(int nb)
{
	int	t;

	t = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while ((t <= nb / 2) && (t < 46342))
	{
		if (t * t == nb)
			return (t);
		t++;
	}
	return (0);
}
