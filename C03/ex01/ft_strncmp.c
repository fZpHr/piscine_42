int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && s2[j] != '\0' && s1[i] == s2[j]
		&& i < n - 1 && j < n - 1)
	{
		j++;
		i++;
	}
	return (s1[i] - s2[j]);
}
