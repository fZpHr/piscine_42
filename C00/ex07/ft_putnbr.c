#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int	a;

	a = nb;
	if (a < 0)
	{
		a = a * (-1);
		write(1, "-", 1);
	}
	if (a <= 9)
	{
		a = a + 48;
		write(1, &a, 1);
	}
	else
	{
		ft_putnbr(a / 10);
		a = a % 10 + 48;
		write(1, &a, 1);
	}
}
